/**
 * Specifies the orientation for the description list. The default `vertical` orientation
 * stacks term-description pairs in a vertical list, while the `horizontal` orientation displays
 * term-description pairs side by side in a horizontal list. This only applies to description lists
 * that use the `nameValuePair` mode.
 */
export type SkyDescriptionListOrientation = 'horizontal' | 'vertical';
