/**
 * Specifies the orientation for the definition list. The default `vertical` orientation
 * stacks label-value pairs in a vertical list, while the `horizontal` orientation displays
 * label-value pairs side by side in a horizontal list. This only applies to definition lists
 * that use the `nameValuePair` mode.
 */
export type SkyDefinitionListOrientation = 'horizontal' | 'vertical';
